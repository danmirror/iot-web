function func_btn1_on(){
    let count =1;
    // console.log(count);
    let element_on = document.getElementById("btn1_id_on");
    let element_off= document.getElementById("btn1_id_off");
    element_on.classList.remove("btn-outline-success");
    element_on.classList.add("btn-success");
    element_off.classList.remove("btn-danger");
    element_off.classList.add("btn-outline-danger");
    document.getElementById('value1').value=count; 
}
